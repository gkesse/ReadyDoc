# QStringListModel 
-> editable model 
-> display a number of strings in a view widget, 
-> such as a QListView or a QComboBox.

# Model indexes corresponding to items are obtained
-> index() 

# item flags are obtained
-> flags()

# Item data is read and written
-> data()
-> setData()

# number of rows
-> rowCount()

# strings can be set later
-> setStringList()

# Strings can also be inserted
-> insertRows()

# Strings can also be removed 
-> removeRows()

# string list can be retrieved 
-> stringList() 

# example usage of QStringListModel:
QStringListModel *model = new QStringListModel();
QStringList list;
list \<< "a" \<< "b" \<< "c";
model->setStringList(list);

