# NDK
-> Native Development Kit
-> Kit de Développement Native

# But
-> Permet de développer des applications Android 
-> dans un langage natif tel que le C/C++.

# Avantages
-> Améliorer les performances d une application Android
-> Qui demande de la puissance processeur
-> Telle que les applications multimédia et de jeux vidéo

















