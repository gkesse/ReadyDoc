# Markdown
-> langage de balisage léger créé par John Gruber en 2004. 
-> offrir une syntaxe facile à lire et à écrire.

# mettre du texte en emphase (balise HTML <em>)
*quelques mots* ou  
_quelques mots_

# mettre du texte en grande emphase (balise HTML <strong>)
**plus important** ou 
__également important__

# mettre du code dans le texte (balise HTML <code>)
Mon texte `code` fin de mon texte

# paragraphe de code, mettre quatre espaces devant
    Première ligne de code
    Deuxième ligne

# faire des citations
> Ce texte apparaîtra dans un élément HTML <blockquote>.

# faire un nouveau paragraphe, sauter une ligne 
Premier paragraphe

Deuxième paragraphe   

# faire un simple retour à la ligne
-> mettre deux espaces en fin de ligne (balise HTML <br>).

# créer une liste non ordonnée (balise HTML <ul>)
* Pommes
* Poires
    * Sous élément avec au moins quatre espaces devant.

# liste ordonnée (balise HTML <ol>) :
1. mon premier
2. mon deuxième

# liste en mode case à cocher
- [ ] Case non cochée
- [x] Case cochée

# titres
-> créés avec un certain nombre de \# avant le titre
----------
\# un titre de premier niveau
\#### un titre de quatrième niveau

# les deux premiers niveaux de titre (<h1> et <h2>)
-> souligner le titre avec des = ou des -
----------
Titre de niveau 1
=====================
Titre de niveau 2
-------------------

# créer des tableaux (balises HTML <tr> et <th>)
| Titre 1       |     Titre 2     |   Titre 3      |
|---------------|-----------------|----------------|
| Colonne       |     Colonne     |      Colonne   |
| Alignée à     |   Alignée au    |     Alignée à  |
| Gauche        |     Centre      |      Droite    |

# créer des liens (balise HTML <a>)
[texte du lien](url_du_lien "texte pour le titre, facultatif")

