# TextEditor

# Faire un zoom avant
CTRL + [+]
----------
-> Augmenter la taille des caractère

# Faire un zoom arrière
CTRL + [-]
----------
-> Réduire la taille des caractère

# Identer les lignes de code
CTRL + A -> Sélectionner
CTRL + I -> Identer
----------
-> Réorganiser les lignes de code

