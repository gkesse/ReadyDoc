#================================================
# VirtualBox (Créer une machine virtuelle)
#================================================

-> Run VirtualBox 
-> and click on the New icon 
-> Set Name to Jenkins_Ubuntu_11.04. 
-> Click on the Next button.
-> Set Memory to 2048 MB 
-> and then click on Next.
-> Select Use existing hard disk. 
-> Browse and select the unpacked VDI image 
-> Press the Create button.

