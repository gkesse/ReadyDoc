# Q_INIT_RESOURCE
Initialise les ressources spécifiées par le fichier.qrc avec 
le nom de base spécifié. 

# QPainter::drawPixmap
Dessine la source de la partie rectangulaire du pixmap donné 
dans la cible donnée dans le dispositif de peinture.

# QObject::setObjectName
Cette propriété contient le nom de cet objet.

# QWidget::setGeometry
Cette propriété contient la géométrie du widget par rapport à 
son parent et à l exclusion du cadre de la fenêtre.

# QGraphicsScene::setBackgroundBrush
Cette propriété contient la brosse de fond de la scène.




