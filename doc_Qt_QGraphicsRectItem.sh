
# QGraphicsRectItem
-> fournit un élément rectangle 
-> que vous pouvez ajouter à un QGraphicsScene.

# setTransform
-> Définit la matrice de transformation actuelle 
-> de l élément en matrice.

# QTransform::fromScale
-> Crée une matrice qui correspond à une mise à l échelle 
-> de sx horizontalement et sy verticalement.

# setPos
-> Définit la position de l élément à pos,
-> qui est en coordonnées parentales. 
-> Pour les éléments sans parent, pos est en coordonnées de scène.

